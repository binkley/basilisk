CREATE TABLE IF NOT EXISTS BASILISK.ZOO
(
    id   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS BASILISK.ANIMAL
(
    id   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR NOT NULL UNIQUE,
    zoo  BIGINT  NOT NULL -- REFERENCES BASILISK.ZOO (ID)
);
